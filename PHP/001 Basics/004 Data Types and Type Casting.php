<?php

// 4 Scalar Types

// bool - true / false
$completed = true;
// int - 1,2,3,0,-5 (no decimal)
$score = 75;
// float - 1.5,0.1,0.005,-15.8
$price = 0.99;
// string - "Shahriar", "Hello World!"
$greeting = "Hello Shahriar";

echo $completed; // Output: 1
echo $score; // Output: 75
echo $price; // Output: 0.99
echo $greeting; // Output: Hello Shahriar
