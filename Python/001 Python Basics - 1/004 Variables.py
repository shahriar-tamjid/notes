# snake_case method

iq = 190
print(iq) # 190

user_iq = 175
print(user_iq) # 175

user_age = iq / 4
print(user_age) # 47.5

# Constants are generally written in capital letters
PI = 3.1415926535
print(PI) # 3.1415926535

# Rapidly assign multiple variables at once
a,b,c = 1,2,3
print(a) # 1
print(b) # 2
print(c) # 3

# Expression vs Statement
# Expression is a part of the code that produces some value
# Statement is a single line of code that performs some action
num = 100
half_of_num = num / 2
# line 25 and 26 are statements
# "num / 2" is an expression

# augmented assignment operator
some_value = 5
some_value += 2
print(some_value) # 7
some_value -= 3
print(some_value) # 4
some_value *= 5
print(some_value) # 20
some_value /= 10
print(some_value) # 2.0
