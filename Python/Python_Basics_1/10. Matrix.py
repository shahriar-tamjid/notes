# Matrix are 2D lists or arrays
# Matrix are very useful in Machine Learning or Image Processing
# Computers can identify pixels by using matrices
matrix = [
    [1,2,3],
    [4,5,6],
    [7,8,9]
]
print(matrix) # [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# We can access a specific item in the matrix too
# Let's say we want to access the 2nd item of the first row in the matrix
print(matrix[0][1]) # 2
# Now we want to access 9 in the matrix
print(matrix[2][2]) # 9
# Now we want to access the entire 2nd row in the matrix
print(matrix[1]) # [4, 5, 6]
