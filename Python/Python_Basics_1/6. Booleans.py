# Booleans
print(bool(1)) # True
print(bool(0)) # False
print(bool("True")) # True
print(bool("False")) # True --> If there is any strings then it's truthy value
print(bool("")) # False --> If there is no string then it's falsy value
print(bool(5)) # True --> If there is any number then it's truthy value
print(bool()) # False --> If there is literally nothing then it's falsy value
print(bool('q')) # True --> If there is any character then it's truthy value
